# Casting

###############################################################################

# Integers

x = int(1)  # 1
y = int(2.8)  # 2
z = int("3")  # 3

print(x, y, z)

###############################################################################

# Floats

x = float(1)  # 1.0
y = float(2.8)  # 2.8
z = float("3")  # 3.0

print(x, y, z)

###############################################################################

# Strings

x = str("s")  # "s"
y = str(2)  # "2"
z = str(3.0)  # "3.0"

print(x, y, z)
